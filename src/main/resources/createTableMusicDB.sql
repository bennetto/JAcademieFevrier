CREATE TABLE ARTISTE(
CODEARTISTE INT PRIMARY KEY,
NOM VARCHAR(50)
);

CREATE TABLE ALBUM(
CODEALBUM INT PRIMARY KEY,
NOM VARCHAR(50),
ARTISTE_ID INT,
FOREIGN KEY (ARTISTE_ID)
REFERENCES ARTISTE (CODEARTISTE) 
);


CREATE TABLE CHANSON(
IDCHANSON INT GENERATED BY DEFAULT AS IDENTITY  PRIMARY KEY,
NUMERO INT,
TITRE VARCHAR(100),
DUREE INT,
ALBUM_ID INT,
FOREIGN KEY (ALBUM_ID)
REFERENCES ALBUM (CODEALBUM) 
);